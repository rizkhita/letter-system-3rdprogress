      <div class="modal fade" id="exampleCutiSakit" tabindex="-1" role="dialog" aria-labelledby="exampleCutiSakit" aria-hidden="true">
      <div class="modal-dialog" role="document">
        <div class="modal-content">
          <div class="modal-header">
            <h5 class="modal-title" id="exampleCutiSakit">
         Ringkasan Peraturan Cuti Sakit 2018</h5>
            <button class="close" type="button" data-dismiss="modal" aria-label="Close">
              <span aria-hidden="true">×</span>
            </button>
          </div>
          <div class="modal-body">
          <br>1. Setiap PNS yang menderita sakit berhak atas cuti sakit.
          <br>2. PNS yang sakit 1 (satu) hari menyampaikan surat keterangan
          sakit secara tertulis kepada atasan langsung dengan
          melampirkan surat keterangan dokter.
          <br>3. PNS yang sakit lebih dari 1 (satu) hari sampai dengan 1<br>4.(empat
          belas) hari berhak atas cuti sakit, dengan ketentuan bahwa PNS
          yang bersangkutan harus mengajukan permintaan secara
          tertulis kepada Pejabat Yang Berwenang Memberikan Cuti
          dengan melampirkan surat keterangan dokter.
          <br>4. PNS yang menderita sakit lebih dari 14 (empat belas) hari berhak
          atas cuti sakit, dengan ketentuan bahwa PNS yang
          bersangkutan harus mengajukan permintaan secara tertulis
          kepada Pejabat Yang Berwenang Memberikan Cuti dengan
          melampirkan surat keterangan dokter pemerintah.
          <br>5. Dokter pemerintah sebagaimana dimaksud dalam angka 4
          merupakan dokter yang berstatus PNS atau dokter yang bekerja
          pada unit pelayanan kesehatan pemerintah.
          <br>6. Surat Keterangan dokter sebagaimana dimaksud pada angka 3
          dan <br>4.paling sedikit memuat pernyataan tentang perlunya
          diberikan cuti, lamanya cuti dan keterangan lain yang
          diperlukan.
          <br>7. Hak atas cuti sakit diberikan untuk waktu paling lama 1 (satu)
          tahun.
          <br>8. Jangka waktu cuti sakit sebagaimana dimaksud pada angka 7
          dapat ditambah untuk paling lama 6 (enam) bulan apabila
          diperlukan, berdasarkan surat keterangan tim penguji
          kesehatan yang ditetapkan oleh menteri yang menyelenggarakan
          urusan pemerintahan di bidang kesehatan.
          <br>9. PNS yang tidak sembuh dari penyakitnya dalam jangka waktu
          sebagaimana dimaksud pada angka 8 harus diuji kembali
          kesehatannya oleh tim penguji kesehatan yang ditetapkan oleh
          menteri yang menyelenggarakan urusan pemerintahan di bidang
          kesehatan.
          <br>10. Apabila berdasarkan hasil pengujian kesehatan sebagaimana
          dimaksud pada angka <br>9.PNS belum sembuh dari penyakitnya,
          PNS yang bersangkutan diberhentikan dengan hormat dari
          jabatannya karena sakit dengan mendapat uang tunggu sesuai
          dengan ketentuan peraturan perundang-undangan.
          <br>11. PNS yang mengalami gugur kandungan berhak atas cuti sakit
          untuk paling lama 1,5 (satu setengah) bulan.
          <br>12.Untuk menggunakan hak atas cuti sakit sebagaimana dimaksud
          pada angka 1, PNS yang bersangkutan mengajukan permintaan
          secara tertulis kepada Pejabat Yang Berwenang Memberikan
          Cuti. 
