<br>&nbsp;&nbsp;f. mendampingi f merawat orang tua/mertua yang sakit/uzrtr.
<br>3. Untuk mengajukan cuti di luar tanggungan negara karena
alasan sebagaimana dimaksud pada angka 2 huruf a harus
melampirkan surat penugasan atau surat perintah tugas
negara/tugas belajar dari pejabat yang berwenang.
<br>4. Untuk mengajukan cuti di luar tanggungan negara karena
alasan sebagaimana dimaksud pada angka 2 huruf b harus
melampirkan surat keputusan atau surat penugasan/
pengangkatan dalam jabatan.
<br>5. Untuk mengajukan cuti di luar tanggungan negara karena
alasan sebagaimana dimaksud pada angka 2 huruf c, huruf d,
dan huruf e harus melampirkan surat keterangan dokter
spesialis.
<br>6. Untuk mengajukan cuti di luar tanggungan negara karena
alasan sebagaimana dimaksud pada angka 2 huruf f harus
melampirkan surat keterangan dokter.
<br>7. Cuti di luar tanggungan negara dapat diberikan untuk paling
lama 3 (tiga) tahun.
<br>8. Jangka waktu cuti di luar tanggungan negara sebagaimana
dimaksud pada angka 7 dapat diperpanjang paling lama 1 (satu)
tahun apabila ada alasan-alasan yang penting untuk
memperpanjangnya.
<br>9. Cuti di luar tanggungan negara mengakibatkan PNS yang
bersangkutan diberhentikan dari jabatannya. 
<br>10. Jabatan yang menjadi lowong karena pemberian cuti di luar
tanggungan negara harus diisi.
<br>11. Untuk mendapatkan cuti di luar tanggungan negara, PNS yang
bersangkutan mengajukan permintaan f permohonan secara
