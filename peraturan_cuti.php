          <div class="modal-body">
          <br>1. PNS berhak atas cuti karena alasan penting, apabila:
          <br>&nbsp;&nbsp;a. ibu, bapak, isteri atau suami, anak, adik, kakak, mertua,
          atau menantu sakit keras atau meninggal dunia;
          <br>&nbsp;&nbsp;b. salah seorang anggota keluarga yang dimaksud pada huruf
          a meninggal dunia, dan menurut peraturan perundangundangan
          PNS yang bersangkutan harus mengurus hakhak
          dari anggota keluarganya yang meninggal dunia; atau
          <br>&nbsp;&nbsp;c. melangsungkan perkawinan.
          <br>2. Sakit keras sebagaimana dimaksud pada angka I huruf a
          dibuktikan dengan melampirkan surat keterangan rawat inap
          dari Unit Pelavanan Kesehatan.
          <br>2. PNS laki-laki yang isterinya melahirkan/operasi caesar dapat
          diberikan cuti karena alasan penting dengan melampirkan surat
          keterangan rawat inap dari Unit Pelayanan Kesehatan.
          <br>3. Dalam hal PNS mengalami musibah kebakaran rumah atau
          bencana alam, dapat diberikan cuti karena alasan penting
          dengan melampirkan surat keterangan paling rendah dari Ketua
          Rukun Tetangga.
          <br>4. PNS yang ditempatkan pada perwakilan Republik Indonesia yang
          rawan dan atau berbahaya dapat mengajukan cuti karena
          alasan penting guna memulihkan kondisi kejiwaan PNS yang
          bersangkutan.
          <br>5. Lamanya cuti karena alasan penting ditentukan oleh Pejabat
          Yang Berwenang Memberikan Cuti paling lama 1 (satu) bulan.
          <br>6. Untuk menggunakan hak atas cuti karena alasan penting
          sebagaimana dimaksud pada angka 1, PNS yang bersangkutan
          mengajukan permintaan secara tertulis kepada Pejabat Yang
          Berwenang Memberikan Cuti.
