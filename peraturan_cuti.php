<br>2. Alasan pribadi dan mendesak sebagaimana dimaksud pada
angka 1 antara lain sebagai berikut:
<br>&nbsp;&nbsp;a. mengikuti atau mendampingi suami/ isteri tugas
negara/tugas belajar di dalam/luar negeri;
<br>&nbsp;&nbsp;b. mendampingi suami/isteri bekerja di dalam/luar negeri;
<br>&nbsp;&nbsp;c. menjalani program untuk mendapatkan keturunan;
<br>&nbsp;&nbsp;d. mendampingi anak yang berkebutuhan khusus;
<br>&nbsp;&nbsp;e. mendampingi suami/isteri/anak yang memerlukan
